cadena = "Este es un ejemplo de Substring (Debanado de cadenas)"

print(len(cadena)) # Devuelve el tamaño de la cadena.
print(cadena[2]) # Devuelve el caracter que está en ese índice.
print(cadena[0 : 10]) # Devuelve la cadena desde el índice inicial hasta el final.
print(cadena[ : 5]) # Devuelve la cadena desde null (0) hasta el índice final.
print(cadena[5 : ]) # Devuelve la cadena desde el índice dado hasta el final.
print(cadena[-5]) # Devuelve el caracter indicado pero inicia a contar desde el final.
print(cadena[-5 : ]) # Devuelve el caracter indicado pero inicia a contar desde el final.