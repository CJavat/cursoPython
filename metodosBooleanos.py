cadena = "Estoy mostrando los metodos booleanos para los strigns"

print(cadena.startswith("E")) # Compara si la cadena empieza con E mayuscula
print(cadena.startswith("e")) # Compara si la cadena empieza con e minuscula
print(cadena.endswith("s")) # Compara si la cadena termina con s minuscula

print(cadena.isupper()) # Compara si la cadena esta toda en mayuscula
print(cadena.islower()) # Compara si la cadena esta toda en minuscula
