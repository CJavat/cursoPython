    # Exponenciación.
    # Multiplicación y División.
    # Suma y Resta.

# SUMA  -----------> +  | Devuelve la suma los números.
# RESTA -----------> -  | Devuelve la resta los números.
# MULTIPLICACIÓN --> *  | Devuelve la multiplicación los números.
# DIVISIÓN --------> /  | Devuelve la división de los números.
# MÓDULO ----------> %  | Devuelve el resto de una división.
# EXPONENTE -------> ** | Devuelve el exponente del número.
# DIVSIÓN ENTERA --> // | Devuelve la división entera de la división.

#EJEMPLOS: 
numero1 = 10
numero2 = 20

print(numero1 + numero2) # SUMA
print(numero1 - numero2) # RESTA
print(numero1 * numero2) # MULTIPLICACIÓN
print(numero1 / numero2) # DIVISIÓN
print(numero1 % numero2) # MÓDULO
print(numero1 ** 2) # EXPONENTE A LA 2
print(numero2 ** 3) # EXPONENTE A LA 3
print(numero1 // numero2) # DIVISIÓN ENTERA
